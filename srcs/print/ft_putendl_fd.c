#include "print.h"

/*
**	@param char	*str
**	@param int fd
**	write a string with a newline to a specific fd
*/

void	ft_putendl_fd(const char *str, int fd)
{
	ft_putstr_fd(str, fd);
	ft_putchar_fd('\n', fd);
}
